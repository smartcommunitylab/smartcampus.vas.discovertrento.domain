package eu.trentorise.smartcampus.domain.discovertrento;

import java.io.Serializable;

public class RatingData implements Serializable {
	private static final long serialVersionUID = -5220746443705936903L;

	private String user;
	private int value;

	public RatingData() {
		super();
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
}
